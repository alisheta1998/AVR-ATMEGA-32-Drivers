#ifndef STD_TYPES_H_
#define STD_TYPES_H_

typedef unsigned char      		U8;
typedef unsigned short int 		U16;
typedef unsigned long int  		U32;
typedef signed char  			S8;
typedef signed short int   		S16;
typedef signed long int 		S32;
typedef float 					F32;
typedef double 					F64;
typedef unsigned char* 			PtrU8;
typedef unsigned short int*		PtrU16;
typedef unsigned long int* 		PtrU32;
typedef signed char* 			PtrS8;
typedef signed short int* 		PtrS16;
typedef signed long int* 		PtrS32;
typedef float* 					PtrF32;
typedef double* 				PtrF64;


#endif /* STD_TYPES_H_ */
